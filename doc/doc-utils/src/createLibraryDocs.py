project_id = os.environ.get("OTC_SDK_PROJECTID")

url = f"https://functiongraph.eu-de.otc.t-systems.com/v2/{project_id}/fgs/functions"


def token_iam(username, password, domain, project="eu-de", https_insecure=False):
    """authenticate a user by password"""
    auth = {
        "auth": {
            "identity": {
                "methods": ["password"],
                "password": {
                    "user": {
                        "name": username,
                        "password": password,
                        "domain": {"name": domain},
                    }
                },
            },
            "scope": {"domain": {"name": domain}, "project": {"id": project}},
        }
    }

    _session = requests.session()
    authstr = json.dumps(auth, indent=4)

    _url = "https://iam.eu-de.otc.t-systems.com:443"

    if "X-Auth-Token" in _session.headers:
        del _session.headers["X-Auth-Token"]

    if https_insecure:
        response = _session.post(
            _url + "/v3/auth/tokens",
            data=authstr,
            verify=False,
            headers={"Content-Type": "application/json;charset=utf8"},
        )
    else:
        response = _session.post(
            _url + "/v3/auth/tokens",
            data=authstr,
            headers={"Content-Type": "application/json;charset=utf8"},
        )
    if response.status_code != 201:
        raise HTTPError(response=response)

    headers = response.headers
    token = headers["x-subject-token"]

    return token


def delete_function(func_urn: str):
    headers = {
        "x-auth-token": token,
        "Content-Type": "application/json;charset=utf8",
    }

    delurl = f"{url}/{func_urn}"[:-7]

    r = requests.request("DELETE", delurl, headers=headers)
    
    if r.status_code != 204:
        raise HTTPError(response=r)



