def create_function(func_name: str, handler_name: str, version: str):

    code = base64.b64encode(open("index.py", "rb").read()).decode("utf-8")

    attrs = {
        "func_name": func_name,
        "package": "default",
        "runtime": version,
        "timeout": 30,
        "handler": handler_name,
        "memory_size": 128,
        "code_type": "inline",
        "func_code": {"file": code},
    }

    headers = {
        "x-auth-token": token,
        "Content-Type": "application/json;charset=utf8",
    }

    r = requests.request("POST", url, headers=headers, data=json.dumps(attrs))
    return json.loads(r.text)


def call_function(func_urn: str):
    headers = {
        "x-auth-token": token,
        "Content-Type": "application/json;charset=utf8",
    }

    body = {
        "key": "Hello FunctionGraph",
    }
    payload = json.dumps(body)

    r = requests.request(
        "POST",
        f"{url}/{func_urn}/invocations",
        headers=headers,
        data=payload.encode("utf-8"),
    )
    return json.loads(r.text)


